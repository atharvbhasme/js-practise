// var
// function scoped
// can be redeclare and updated

var name = 'atharv'
var surname= 'bhasme'
console.log(name + ' ' + surname);

// let
// Block Scope
// Cannot redeclare in same block but can be updated
let age = 20;
age = 25
console.log(age);

// const
// Block Scope
// Cannot be redeclared or updated
const birthYear = 2000;
console.log(birthYear);


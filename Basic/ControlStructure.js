// if else
let score = 80;
if (score > 90) {
  console.log('Excellent');
} else {
  console.log('Good');
}

// switch
let color = 'red';
switch (color) {
  case 'blue':
    console.log('Blue!');
    break;
  case 'red':
    console.log('Red!');
    break;
  default:
    console.log('Unknown color');
}

// for
for (let i = 0; i < 3; i++) {
  console.log(i);
}

// while
let i = 0;
while (i < 3) {
  console.log(i);
  i++;
}

// for of
let fruits = ['apple', 'banana'];
for (let fruit of fruits) {
  console.log(fruit);
}


